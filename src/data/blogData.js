export function getBlogPost(slug) {
  return blogPosts.find(p => p.slug === slug) || null
}

export function getRecentPosts(count = 6) {
  return [...blogPosts].sort((a, b) => new Date(b.date) - new Date(a.date)).slice(0, count)
}

export function getRelatedPosts(slug, count = 3) {
  const post = getBlogPost(slug)
  if (!post) return []
  return blogPosts.filter(p => p.slug !== slug && p.category === post.category).slice(0, count)
}
